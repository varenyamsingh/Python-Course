# today we are doing typecasting: the conversion of one datatype to another data type.
a = "1"
b = "2"
print(a + b)#this will give 12 because we have put those numbers in quotation sign soo they are recognized as string so now we will apply typecasting
print(int(a) + int(b)) #so this will convert our string into integer
